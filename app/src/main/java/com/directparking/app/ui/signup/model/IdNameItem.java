package com.directparking.app.ui.signup.model;

public class IdNameItem {

    private String id;
    private String name;

    public IdNameItem(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
